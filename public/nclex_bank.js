function getAllQuestions() {
  const all = [];
  ['pharmacology', 'prioritization', 'safety', 'labValues'].forEach(section => {
    if (NCLEX_BANK[section] && NCLEX_BANK[section].questions) {
      NCLEX_BANK[section].questions.forEach(q => {
        all.push({ ...q, section: section });
      });
    }
  });
  return all;
}

function getQuestionsBySection(sectionKey) {
  return NCLEX_BANK[sectionKey]?.questions || [];
}

function getQuestionsByDifficulty(difficulty) {
  return getAllQuestions().filter(q => q.difficulty === difficulty);
}

function getQuestionsByTopic(topic) {
  return getAllQuestions().filter(q => 
    q.topic.toLowerCase().includes(topic.toLowerCase())
  );
}

function getRandomQuiz(count = 10, sectionKey = null) {
  let pool = sectionKey ? getQuestionsBySection(sectionKey) : getAllQuestions();
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
}

function getSectionSummary() {
  const summary = {};
  ['pharmacology', 'prioritization', 'safety', 'labValues'].forEach(key => {
    if (NCLEX_BANK[key]) {
      summary[key] = {
        name: NCLEX_BANK[key].name,
        icon: NCLEX_BANK[key].icon,
        weight: NCLEX_BANK[key].weight,
        questionCount: NCLEX_BANK[key].questions.length
      };
    }
  });
  return summary;
}

function getQuestionById(id) {
  return getAllQuestions().find(q => q.id === id);
}

